<p>Kirby's Adventure es la segunda aventura de la bola rosa Kirby. Lanzado en los últimos tiempos de vida de la NES, tenia unos gráficos consierablemente buenos y bonitos para la máquina en la que se hizo. Su variedad en escenarios y situaciones, el encanto y alegría que rebosan los gráficos, junto a una jugabilidad divertida y con controles precisos lo hicieron uno de los mejores juegos del catálogo de la consola.</p>
<hr/>
<p>Un viejo proyecto, empezado allá por el 2008, por dos traductores entusiastas y un pátetico intento de romhacker que no supo ver un proyecto que le quedaba grande. Tras un larguisimo <i>hiatus</i> el juego pudo ser hackedado con éxito por Pacochan, y servidor pudo terminar lo que 3 años antes no pudo. </p>
<p>Este proyecto no solo se ha terminado tanto tiempo despues por el interés que tenía en traducir Kirby's Adventure, sino también por disculparme por no haber estado a la altura de las ilusiones que Cygnus y SkaDs, mis compañeros, tenian de traducirlo.</p>
<p>Finalmente aquí está. La traducción está relativamente cerca de ser perfecta y totalmente completa, aunque se ha de decir que el juego era internamente absurdamente enrevesado y dio bastante guerra. El archivo comprimido, ademas del Leeme, incluye tres parches, para usar sobre la rom Americana, Europea o Francesa, según cual se tenga más a mano.</p>
