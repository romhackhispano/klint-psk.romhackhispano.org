<h1>Enlaces</h1>
<h3>Enlaces a otras webs que considero de interés</h3>
<a href="http://blog.tales-tra.com">Tales Translations</a>
<p>El primer grupo al que pertenecí y pertenezco, incluso antes de la traducción
del Super Metroid. El foro ya no es lo que era, pero en mi opinión sigue siendo
un lugar especial por el que vale la pena pasarse.</p>
<a href="http://pacochan.tales-tra.com/">La Puta Web de PacoChan</a>
<p>La web personal de Pacochan, miembro fundador de Tales Translations y una
gran persona y amigo. También hace traducciones de excelente calidad y hasta
juegos <i>indie</i>.</p>
<a href="http://www.romhackhispano.org">Romhack hispano</a>
<p>El lugar ideal a donde ir a preguntar si tienes dudas sobre un tema
de romhack. No es el foro más vivo del mundo, pero somos mayormente amables.
En las páginas de algunos de los grupos puedes encontrar también cosas interesantes
como tutoriales (a destacar el inmortal tutorial de Pablito's, <a href="http://sayans.romhackhispano.org/old/documentos/manual_de_traduccion_de_videojuegos.pdf">"El Fascinante
Mundo del Romhacking"</a>).</p>
<a href="http://romhacking.net/">Romhacking dot net</a>
<p>Otro gran lugar para los romhackers. Tienen abundante documentación, aplicaciones,
foro para ayudas, base de datos de traducciones y hacks ya hechos. Vale la pena
echarle un vistazo de vez en cuando.</p>
<a href="http://klint-psk.blogspot.com">Mi blog</a>
<p>Mi blog personal, quizás demasiado personal para ser público. Actualmente
un tanto abandonado.</p>
<a href="http://www.ibidemgroup.es">Ibidem Group</a>
<p>Agencia de traducción que suele publicar interesantes ofertas de trabajo
para traductores.</p>
