<h1>Acerca de...</h1>
    <p>Klint Proguctions surgió en 2007, cuando terminé la traducción al
    español de Super Metroid. Traducción mejorable, y que pretendo mejorar en un futuro no muy lejano.
    Tras dos traducciones más, ambas de una dificultad ridícula, me
    propuse traducir el Kirby Adventures junto con Cygnus y SkadS...
    traducción que tardó tres años más en ser terminada y publicada, y no sin requerir ayuda, además. El resto de proyectos se congelaron, también.
    </p>
    <p>Tras casi dos años de total silencio volví con intención de retomar mis viejos proyectos y, al menos, 
    rendir cuentas con mis pasados proyectos. Lamentablemente un año después de esta decisión siguió otro largo <i>hiatus</i>.
    </p>
    <p>Ahora he vuelto una vez más, con más ganas, más centrado, y con más gente que puede ayudarme (y a la que nunca le estaré lo bastante agradecido). 
    Me gustaría seguir dedicándome al romhacking y que esta página tenga al menos algo de actividad.</p>
