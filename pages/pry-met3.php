<p>Super Metroid es un juego de SNES del año 1994. Máximo exponente
de la old school de la saga Metroid, este juego ofrecía enormes
posibilidades y muchas horas de entretenimiento, además de ser
bastante épico de por si.</p>
<hr/>
<p>La traducción comenzó hace mucho tiempo... tanto que ni lo recuerdo.
Pero la "terminé" en marzo del 2008. Digo terminé entre comillas
porque era mi primera traducción y tenía y tiene muchísimas cosas 
mejorables. Planeo una nueva versión en cuanto termine el Fire Emblem, que
traducirá las cosas que quedaron en inglés, aunque esta traducción 
no es prioritaria.</p>
<p>A pesar de no estar especialmente orgulloso de esta versión, 
seria tonto no poner a disposicion del público la versión 1, la
inicial que liberé en marzo del 2008. Es muy mejorable como he
dicho, así que bajadla bajo vuestro riesgo. La traducción debería
funcionar con las versiones americanas y japonesas del juego. No
sé, hasta la fecha, una forma de aplicar el parche al Super Metroid
que se puede descargar de la consola virtual de la Wii, por desgracia.
</p>
